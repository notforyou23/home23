  /**
   * Generate metadata JSON from settings
   */
  generateMetadata(settings, cleanStart = false) {
    const {
      exploration_mode = 'guided',
      domain = '',
      context = '',
      execution_mode = 'mixed',
      depth = 'normal',
      max_cycles = '100',
      silent_planning = false,
      enable_web_search = true,
      enable_sleep = true,
      enable_coding_agents = true,
      enable_stabilization = false,
      enable_github_mcp = false,
      enable_experimental = false,
      enable_introspection = true,
      enable_agent_routing = true,
      enable_memory_governance = true,
      enable_recursive_mode = true,
      enable_frontier = true,
      enable_capabilities = true,
      frontier_mode = 'observe',
      review_period = 20,
      max_concurrent = 4,
      file_access_paths = 'runtime/outputs/, runtime/exports/',
      cluster_enabled = false,
      cluster_backend = 'none',
      cluster_size = 1,
      specialization_enabled = false,
      specialization_profiles = [],
      enable_local_llm = false,
      local_llm_base_url = 'http://localhost:11434/v1',
      local_llm_default_model = 'qwen2.5:14b',
      local_llm_fast_model = 'qwen2.5:14b',
      searxng_url = ''
    } = settings || {};

    const maxCyclesValue = max_cycles === 'unlimited' ? 'null' : max_cycles;

    // Build specialization metadata
    let specializationMeta = {
      profiles: '',
      agentTypes: '',
      keywords: '',
      tags: '',
      avoidKeywords: '',
      avoidTags: ''
    };

    if (specialization_enabled && specialization_profiles && specialization_profiles.length > 0) {
      const profiles = [];
      const agentTypes = [];
      const keywords = [];
      const tags = [];
      const avoidKeywords = [];
      const avoidTags = [];

      for (let i = 0; i < cluster_size; i++) {
        const instanceLabel = `cosmo-${i + 1}`;
        const profile = specialization_profiles[i] || {};

        profiles.push(`${instanceLabel}=${profile.name || 'generalist'}`);
        agentTypes.push(`${instanceLabel}=${JSON.stringify(profile.agentTypes || [])}`);
        keywords.push(`${instanceLabel}=${JSON.stringify(profile.keywords || [])}`);
        tags.push(`${instanceLabel}=${JSON.stringify(profile.tags || [])}`);
        avoidKeywords.push(`${instanceLabel}=${JSON.stringify(profile.avoidKeywords || [])}`);
        avoidTags.push(`${instanceLabel}=${JSON.stringify(profile.avoidTags || [])}`);
      }

      specializationMeta.profiles = profiles.join('|');
      specializationMeta.agentTypes = agentTypes.join('|');
      specializationMeta.keywords = keywords.join('|');
      specializationMeta.tags = tags.join('|');
      specializationMeta.avoidKeywords = avoidKeywords.join('|');
      specializationMeta.avoidTags = avoidTags.join('|');
    }

    const metadata = {
      created: new Date().toISOString(),
      cleanStart,
      explorationMode: exploration_mode,
      domain,
      context,
      executionMode: execution_mode,
      depth,
      maxCycles: maxCyclesValue,
      silentPlanning: silent_planning,
      enableWebSearch: enable_web_search,
      enableSleep: enable_sleep,
      enableCodingAgents: enable_coding_agents,
      enableStabilization: enable_stabilization,
      githubMcpEnabled: enable_github_mcp,
      enableExperimental: enable_experimental,
      enableIntrospection: enable_introspection,
      enableAgentRouting: enable_agent_routing,
      enableMemoryGovernance: enable_memory_governance,
      enableRecursiveMode: enable_recursive_mode,
      enableFrontier: enable_frontier,
      enableCapabilities: enable_capabilities,
      frontierMode: frontier_mode,
      reviewPeriod: review_period,
      maxConcurrent: max_concurrent,
      fileAccessEnabled: true,
      fileAccessPaths: file_access_paths,
      clusterEnabled: cluster_enabled,
      clusterBackend: cluster_backend,
      clusterSize: cluster_size,
      clusterDashboardPort: 3360,
      clusterFilesystemRoot: cluster_backend === 'filesystem' ? 'runtime/cluster' : null,
      clusterSpecializationEnabled: specialization_enabled,
      clusterSpecializationProfiles: specializationMeta.profiles,
      clusterSpecializationAgentTypes: specializationMeta.agentTypes,
      clusterSpecializationKeywords: specializationMeta.keywords,
      clusterSpecializationTags: specializationMeta.tags,
      clusterSpecializationAvoidKeywords: specializationMeta.avoidKeywords,
      clusterSpecializationAvoidTags: specializationMeta.avoidTags,
      // Local LLM settings
      enableLocalLlm: enable_local_llm,
      localLlmBaseUrl: enable_local_llm ? local_llm_base_url : null,
      localLlmDefaultModel: enable_local_llm ? local_llm_default_model : null,
      localLlmFastModel: enable_local_llm ? local_llm_fast_model : null,
      searxngUrl: searxng_url || null,
      launcherVersion: '2.0-web'
    };

    return metadata;
  }

  /**
   * Write config.yaml file
   */
  async writeConfig(settings) {
    const configYaml = await this.generateConfig(settings);
    const configPath = path.join(this.cosmoRoot, 'src', 'config.yaml');

    try {
      await fs.writeFile(configPath, configYaml, 'utf8');
      this.logger.info(`Config written: ${configPath}`);
      return { success: true, path: configPath };
    } catch (error) {
      this.logger.error('Failed to write config:', error);
      return { success: false, error: error.message };
    }
  }

  /**
   * Write metadata JSON file
   */
  async writeMetadata(runPath, settings, cleanStart = false) {
    const metadata = this.generateMetadata(settings, cleanStart);
    const metadataPath = path.join(runPath, 'run-metadata.json');

    try {
      await fs.writeFile(metadataPath, JSON.stringify(metadata, null, 2), 'utf8');
      this.logger.info(`Metadata written: ${metadataPath}`);
      return { success: true, path: metadataPath, metadata };
    } catch (error) {
      this.logger.error('Failed to write metadata:', error);
      return { success: false, error: error.message };
    }
  }

